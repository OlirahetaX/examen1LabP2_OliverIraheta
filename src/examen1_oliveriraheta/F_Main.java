    private void tp_crudStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tp_crudStateChanged
        if (tp_crud.getSelectedIndex() == 0) {
            tf_ip.setText("  Ej: 000.000.0.00");
            tf_ip.setForeground(Color.gray);
            tf_hostname.setText("  Ej: Nuila");
            tf_hostname.setForeground(Color.gray);
            tf_mask.setText("  Ej: 000.000.0.00");
            tf_mask.setForeground(Color.gray);
            tf_almacenamiento.setText(" Ej: 500");
            tf_almacenamiento.setForeground(Color.gray);
            tf_ram.setText(" Ej: 32");
            tf_ram.setForeground(Color.gray);
            tf_pantalla.setText(" Ej: 1920x1080");
            tf_pantalla.setForeground(Color.gray);
            tf_marca.setText(" Ej: HP");
            tf_marca.setForeground(Color.gray);
        }
        if (tp_crud.getSelectedIndex() == 1) {
            String listar = "";
            for (PC pc : pcs) {

                listar += pcs.indexOf(pc) + ")\n" + pc + "\n";
            }
            ta_listar.setText(listar);
        }
        if (tp_crud.getSelectedIndex() == 3) {
            F_crud.setVisible(false);
            this.setVisible(true);
            
        }
    }//GEN-LAST:event_tp_crudStateChanged


